getReduceResults = function(reg, job.ids) {

  df = reduceResultsExperiments(
    reg = reg,
    ids = job.ids,
    fun = function(job, res) {
      return(res)
    }
  )

  colnames(df)[7:9] = c("pred.accuracy", "training.time", "testing.time") 

  return(df)
}

